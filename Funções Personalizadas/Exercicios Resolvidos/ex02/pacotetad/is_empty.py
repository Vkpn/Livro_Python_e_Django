def vazio(pilha):
    """
    Função para retornar se a pilha está vazia ou não.
    :param pilha: recebe a pilha como parâmetro para verificação.
    :return: retorna True se a pilha estiver vazia, caso contrario retorna False.
    """
    if len(pilha) == 0:
        return True
    else:
        return False
